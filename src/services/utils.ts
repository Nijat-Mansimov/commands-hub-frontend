/**
 * Copy text to clipboard with feedback
 */
export const copyToClipboard = async (text: string, label: string = 'Copied'): Promise<boolean> => {
  try {
    await navigator.clipboard.writeText(text);
    return true;
  } catch (err) {
    console.error('Failed to copy:', err);
    return false;
  }
};

/**
 * Download text as file
 */
export const downloadAsFile = (content: string, filename: string) => {
  const element = document.createElement('a');
  element.setAttribute('href', 'data:text/plain;charset=utf-8,' + encodeURIComponent(content));
  element.setAttribute('download', filename);
  element.style.display = 'none';
  document.body.appendChild(element);
  element.click();
  document.body.removeChild(element);
};

/**
 * Export command as PowerShell script
 */
export const exportAsScript = (command: string, filename: string = 'command.ps1') => {
  const script = `# PowerShell Script Generated by Attack Payload System\n# Generated: ${new Date().toISOString()}\n\n${command}`;
  downloadAsFile(script, filename);
};

/**
 * Highlight placeholder syntax in command template
 */
export const highlightPlaceholders = (text: string): string => {
  return text.replace(/\{\{(\w+)\}\}/g, '<span class="text-orange-500 font-semibold">{{$1}}</span>');
};

/**
 * Extract placeholders from command template
 */
export const extractPlaceholders = (template: string): string[] => {
  const regex = /\{\{(\w+)\}\}/g;
  const placeholders: string[] = [];
  let match;

  while ((match = regex.exec(template)) !== null) {
    placeholders.push(match[1]);
  }

  return [...new Set(placeholders)]; // Remove duplicates
};

/**
 * Validate required fields
 */
export const validateFieldValues = (
  fields: Array<{ name: string; type: string; required: boolean }>,
  values: Record<string, string>
): { valid: boolean; errors: Record<string, string> } => {
  const errors: Record<string, string> = {};

  for (const field of fields) {
    if (field.required && (!values[field.name] || values[field.name].trim() === '')) {
      errors[field.name] = `${field.name} is required`;
    }

    if (values[field.name]) {
      const error = validateFieldType(field.type, values[field.name]);
      if (error) {
        errors[field.name] = error;
      }
    }
  }

  return {
    valid: Object.keys(errors).length === 0,
    errors,
  };
};

/**
 * Validate individual field based on type
 */
export const validateFieldType = (type: string, value: string): string | null => {
  if (!value) return null;

  switch (type) {
    case 'email':
      return /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(value) ? null : 'Invalid email format';

    case 'ip':
      return /^(\d{1,3}\.){3}\d{1,3}$|^([0-9a-f]{0,4}:){2,7}[0-9a-f]{0,4}$/i.test(value)
        ? null
        : 'Invalid IP address';

    case 'port':
      const port = parseInt(value, 10);
      return port >= 1 && port <= 65535 ? null : 'Port must be between 1 and 65535';

    case 'url':
      return /^https?:\/\/.+/i.test(value) ? null : 'Invalid URL (must start with http:// or https://)';

    case 'domain':
      return /^([a-z0-9](-[a-z0-9])*\.)+[a-z]{2,}$/i.test(value) ? null : 'Invalid domain format';

    case 'number':
      return /^-?\d+(\.\d+)?$/.test(value) ? null : 'Invalid number format';

    default:
      return null;
  }
};

