/**
 * Parse category string into major and subcategory
 */
export const parseCategory = (categoryString: string): { major: string; subcategory: string } | null => {
  const parts = categoryString.split(' - ');
  if (parts.length === 2) {
    return {
      major: parts[0],
      subcategory: parts[1],
    };
  }
  return null;
};

/**
 * Get major category from full category string
 */
export const getMajorCategory = (categoryString: string): string => {
  const parsed = parseCategory(categoryString);
  return parsed?.major || categoryString;
};

/**
 * Get subcategory from full category string
 */
export const getSubcategory = (categoryString: string): string => {
  const parsed = parseCategory(categoryString);
  return parsed?.subcategory || '';
};

